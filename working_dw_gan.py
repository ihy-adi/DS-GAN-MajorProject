# Define a function to denormalize tensors for visualization
def denormalize(tensor):
    tensor = tensor.clone().detach()
    tensor = tensor.squeeze(0)
    tensor = tensor.cpu().numpy()
    tensor = np.transpose(tensor, (1, 2, 0))
    tensor = ((tensor * 0.5) + 0.5) * 255.0
    tensor = np.clip(tensor, 0, 255).astype(np.uint8)
    return tensor

# Define function to calculate metrics
def calculate_metrics(hazy_path, gt_path, generator, device):
    # Load images
    hazy_img = Image.open(hazy_path).convert('RGB')
    gt_img = Image.open(gt_path).convert('RGB')

    # Resize images if needed to match dimensions
    hazy_width, hazy_height = hazy_img.size
    gt_width, gt_height = gt_img.size

    if hazy_width != gt_width or hazy_height != gt_height:
        gt_img = gt_img.resize((hazy_width, hazy_height), Image.LANCZOS)

    # Apply transformations
    hazy_tensor = transform(hazy_img).unsqueeze(0).to(device)

    # Measure processing time
    start_time = time.time()

    # Generate dehazed image
    with torch.no_grad():
        dehazed_tensor = generator(hazy_tensor)

    # Calculate processing time
    end_time = time.time()
    processing_time = end_time - start_time

    # Convert back to numpy for metric calculation
    dehazed_img = denormalize(dehazed_tensor)

    # Convert ground truth to numpy
    gt_np = np.array(gt_img)

    # Calculate metrics
    try:
        img_psnr = psnr(gt_np, dehazed_img)
        img_ssim = ssim(gt_np, dehazed_img, multichannel=True, data_range=255, channel_axis=2)
    except Exception as e:
        print(f"Error calculating metrics: {e}")
        img_psnr = 0
        img_ssim = 0

    return img_psnr, img_ssim, dehazed_img, processing_time

