def train_model(generator, discriminator, train_loader, val_loader, num_epochs=50):
    # Lists to store metrics
    train_losses_G = []
    train_losses_D = []
    val_psnr_values = []
    val_ssim_values = []

    # Training loop
    for epoch in range(num_epochs):
        generator.train()
        discriminator.train()

        # Track losses
        epoch_loss_G = 0.0
        epoch_loss_D = 0.0

        start_time = time.time()

        for i, (hazy, target) in enumerate(train_loader):
            hazy = hazy.to(device)
            target = target.to(device)

            # Ensure input shapes are correct
            batch_size = hazy.size(0)

            # Check if the discriminator output size is compatible with our labels
            # We'll create labels based on the actual output size
            fake_output = discriminator(generator(hazy))
            output_shape = fake_output.shape

            # Real and fake labels based on actual output size
            real_label = torch.ones(batch_size, *output_shape[1:]).to(device)
            fake_label = torch.zeros(batch_size, *output_shape[1:]).to(device)

            #---------------------
            # Train Discriminator
            #---------------------
            optimizer_D.zero_grad()

            # Generate fake image
            fake_image = generator(hazy)

            # Real loss
            pred_real = discriminator(target)
            loss_real = criterion_GAN(pred_real, real_label)

            # Fake loss
            pred_fake = discriminator(fake_image.detach())
            loss_fake = criterion_GAN(pred_fake, fake_label)

            # Total discriminator loss
            loss_D = 0.5 * (loss_real + loss_fake)

            loss_D.backward()
            optimizer_D.step()

            #---------------------
            # Train Generator
            #---------------------
            optimizer_G.zero_grad()

            # GAN loss
            pred_fake = discriminator(fake_image)
            loss_GAN = criterion_GAN(pred_fake, real_label)

            # Pixel-wise loss
            loss_pixel = criterion_pixelwise(fake_image, target)

            # Total generator loss
            loss_G = loss_GAN + lambda_pixel * loss_pixel

            loss_G.backward()
            optimizer_G.step()

            # Update losses
            epoch_loss_G += loss_G.item()
            epoch_loss_D += loss_D.item()

            # Print progress
            if (i+1) % 20 == 0:
                print(f"Epoch [{epoch+1}/{num_epochs}], Step [{i+1}/{len(train_loader)}], "
                      f"Loss D: {loss_D.item():.4f}, Loss G: {loss_G.item():.4f}")

        # Update learning rates
        scheduler_G.step()
        scheduler_D.step()

        # Calculate average epoch losses
        avg_loss_G = epoch_loss_G / len(train_loader)
        avg_loss_D = epoch_loss_D / len(train_loader)
        train_losses_G.append(avg_loss_G)
        train_losses_D.append(avg_loss_D)

        # Validation after each epoch
        val_psnr, val_ssim = validate_model(generator, val_loader)
        val_psnr_values.append(val_psnr)
        val_ssim_values.append(val_ssim)

        epoch_time = time.time() - start_time

        print(f"Epoch [{epoch+1}/{num_epochs}] completed in {epoch_time:.2f}s - "
              f"Train Loss G: {avg_loss_G:.4f}, Train Loss D: {avg_loss_D:.4f}, "
              f"Val PSNR: {val_psnr:.2f}, Val SSIM: {val_ssim:.4f}")

        # Save models periodically
        if (epoch+1) % 10 == 0:
            torch.save(generator.state_dict(), f'/content/generator_epoch_{epoch+1}.pth')
            torch.save(discriminator.state_dict(), f'/content/discriminator_epoch_{epoch+1}.pth')

    # Save final models
    torch.save(generator.state_dict(), '/content/generator_final.pth')
    torch.save(discriminator.state_dict(), '/content/discriminator_final.pth')

    return train_losses_G, train_losses_D, val_psnr_values, val_ssim_values

