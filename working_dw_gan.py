def validate_model(generator, val_loader):
    generator.eval()
    avg_psnr = 0
    avg_ssim = 0
    count = 0

    with torch.no_grad():
        for hazy, target in val_loader:
            hazy = hazy.to(device)
            target = target.to(device)

            # Generate output
            output = generator(hazy)

            # Convert to numpy arrays for PSNR and SSIM calculation
            # Denormalize images: (img * 0.5 + 0.5) scales from [-1,1] to [0,1]
            output_np = output.detach().cpu().numpy()
            target_np = target.detach().cpu().numpy()

            # Calculate PSNR and SSIM for batch
            for j in range(output_np.shape[0]):
                # Transpose from [C,H,W] to [H,W,C] and denormalize
                out_img = np.transpose(output_np[j], (1, 2, 0)) * 0.5 + 0.5
                target_img = np.transpose(target_np[j], (1, 2, 0)) * 0.5 + 0.5

                # Clip values to [0,1] range
                out_img = np.clip(out_img, 0, 1)
                target_img = np.clip(target_img, 0, 1)

                # Calculate PSNR
                psnr_val = psnr(target_img, out_img, data_range=1)

                # Calculate SSIM with appropriate window size
                # Get minimum dimension and ensure win_size is odd and smaller
                min_dim = min(out_img.shape[0], out_img.shape[1])
                win_size = min(7, min_dim - (min_dim % 2) - 1)  # Make sure it's odd and smaller than min_dim

                # Ensure win_size is at least 3 (minimum for SSIM)
                win_size = max(3, win_size)

                try:
                    # Use channel_axis for multichannel images (RGB)
                    ssim_val = ssim(target_img, out_img, win_size=win_size,
                                   data_range=1, channel_axis=2)
                except Exception as e:
                    print(f"SSIM error: {e}")
                    # Fallback value if SSIM fails
                    ssim_val = 0

                avg_psnr += psnr_val
                avg_ssim += ssim_val
                count += 1

    # Calculate average PSNR and SSIM
    if count > 0:
        avg_psnr /= count
        avg_ssim /= count
    else:
        avg_psnr = 0
        avg_ssim = 0

    return avg_psnr, avg_ssim

