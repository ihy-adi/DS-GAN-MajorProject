# Define the depthwise separable convolution block based on your model's architecture
class DepthwiseSeparableConv(nn.Module):
    def __init__(self, in_channels, out_channels, kernel_size=3, stride=1, padding=1, bias=True):
        super(DepthwiseSeparableConv, self).__init__()
        self.depthwise = nn.Conv2d(in_channels, in_channels, kernel_size=kernel_size,
                                   stride=stride, padding=padding, groups=in_channels, bias=bias)
        self.pointwise = nn.Conv2d(in_channels, out_channels, kernel_size=1, bias=bias)
        self.bn = nn.BatchNorm2d(out_channels)

    def forward(self, x):
        x = self.depthwise(x)
        x = self.pointwise(x)
        x = self.bn(x)
        return x

# Define residual block based on your model's architecture
class ResidualBlock(nn.Module):
    def __init__(self, channels):
        super(ResidualBlock, self).__init__()
        self.conv1 = DepthwiseSeparableConv(channels, channels)
        self.conv2 = DepthwiseSeparableConv(channels, channels)
        self.relu = nn.ReLU(inplace=True)

    def forward(self, x):
        residual = x
        out = self.relu(self.conv1(x))
        out = self.conv2(out)
        out += residual
        return out

# Define your actual generator architecture based on the error message
class Generator(nn.Module):
    def __init__(self):
        super(Generator, self).__init__()

        # Initial layer
        self.initial = nn.Sequential(
            nn.Conv2d(3, 64, kernel_size=7, padding=3),
            nn.ReLU(inplace=True)
        )

        # Downsampling layers
        self.down1 = DepthwiseSeparableConv(64, 128)
        self.down2 = DepthwiseSeparableConv(128, 256)

        # Residual blocks
        self.res_blocks = nn.ModuleList([ResidualBlock(256) for _ in range(6)])

        # Upsampling layers
        self.up1 = DepthwiseSeparableConv(256, 128)
        self.up2 = DepthwiseSeparableConv(128, 64)

        # Output layer
        self.output = nn.Conv2d(64, 3, kernel_size=7, padding=3)

        self.relu = nn.ReLU(inplace=True)
        self.tanh = nn.Tanh()

    def forward(self, x):
        # Initial and downsampling
        out = self.initial(x)
        out = self.relu(self.down1(out))
        out = self.relu(self.down2(out))

        # Residual blocks
        for res_block in self.res_blocks:
            out = res_block(out)

        # Upsampling
        out = self.relu(self.up1(out))
        out = self.relu(self.up2(out))

        # Output
        out = self.tanh(self.output(out))

        # Skip connection from input to output
        return torch.clamp(out + x, -1, 1)

