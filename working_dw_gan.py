class DWBlock(nn.Module):
    """Depth-wise separable convolution block"""
    def __init__(self, in_channels, out_channels, kernel_size=3, stride=1, padding=1):
        super(DWBlock, self).__init__()
        self.depthwise = nn.Conv2d(in_channels, in_channels, kernel_size=kernel_size,
                                   stride=stride, padding=padding, groups=in_channels)
        self.pointwise = nn.Conv2d(in_channels, out_channels, kernel_size=1, stride=1, padding=0)
        self.bn = nn.BatchNorm2d(out_channels)
        # Changed to inplace=False to avoid gradient computation issues
        self.relu = nn.LeakyReLU(0.2, inplace=False)

    def forward(self, x):
        x = self.depthwise(x)
        x = self.pointwise(x)
        x = self.bn(x)
        x = self.relu(x)
        return x

class ResidualBlock(nn.Module):
    """Residual block with depthwise separable convolutions"""
    def __init__(self, channels):
        super(ResidualBlock, self).__init__()
        self.conv1 = DWBlock(channels, channels)
        self.conv2 = DWBlock(channels, channels)

    def forward(self, x):
        residual = x
        out = self.conv1(x)
        out = self.conv2(out)
        # Use addition operation rather than +=
        out = out + residual  # Skip connection
        return out

class Generator(nn.Module):
    def __init__(self, input_channels=3, output_channels=3, num_residual_blocks=8):
        super(Generator, self).__init__()

        # Initial convolution
        self.initial = nn.Sequential(
            nn.Conv2d(input_channels, 64, kernel_size=7, stride=1, padding=3),
            # Changed to inplace=False
            nn.LeakyReLU(0.2, inplace=False)
        )

        # Downsampling layers
        self.down1 = DWBlock(64, 128, kernel_size=3, stride=2, padding=1)
        self.down2 = DWBlock(128, 256, kernel_size=3, stride=2, padding=1)

        # Residual blocks
        self.res_blocks = nn.ModuleList([ResidualBlock(256) for _ in range(num_residual_blocks)])

        # Upsampling layers
        self.up1 = nn.Sequential(
            nn.ConvTranspose2d(256, 128, kernel_size=3, stride=2, padding=1, output_padding=1),
            nn.BatchNorm2d(128),
            # Changed to inplace=False
            nn.LeakyReLU(0.2, inplace=False)
        )

        self.up2 = nn.Sequential(
            nn.ConvTranspose2d(128, 64, kernel_size=3, stride=2, padding=1, output_padding=1),
            nn.BatchNorm2d(64),
            # Changed to inplace=False
            nn.LeakyReLU(0.2, inplace=False)
        )

        # Output layer
        self.output = nn.Sequential(
            nn.Conv2d(64, output_channels, kernel_size=7, stride=1, padding=3),
            nn.Tanh()
        )

    def forward(self, x):
        # Initial convolution
        x = self.initial(x)

        # Downsampling
        x = self.down1(x)
        x = self.down2(x)

        # Residual blocks
        for res_block in self.res_blocks:
            x = res_block(x)

        # Upsampling
        x = self.up1(x)
        x = self.up2(x)

        # Output
        x = self.output(x)

        return x

class Discriminator(nn.Module):
    def __init__(self, input_channels=3):
        super(Discriminator, self).__init__()

        # Series of depth-wise separable convolutions with increasing depth
        self.model = nn.Sequential(
            # Input: 3 x 256 x 256
            DWBlock(input_channels, 64, kernel_size=4, stride=2, padding=1),  # 64 x 128 x 128
            DWBlock(64, 128, kernel_size=4, stride=2, padding=1),  # 128 x 64 x 64
            DWBlock(128, 256, kernel_size=4, stride=2, padding=1),  # 256 x 32 x 32
            DWBlock(256, 512, kernel_size=4, stride=2, padding=1),  # 512 x 16 x 16
            DWBlock(512, 512, kernel_size=4, stride=2, padding=1),  # 512 x 8 x 8

            # Final layer to produce a patch output
            nn.Conv2d(512, 1, kernel_size=4, stride=1, padding=1)  # 1 x 7 x 7
        )

    def forward(self, x):
        return self.model(x)

